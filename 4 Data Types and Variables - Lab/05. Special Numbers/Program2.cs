namespace SpecialNumber
{
    class Program
    {
        public static void Main()
        {
            int number = int.Parse(Console.ReadLine());
            for (int i = 1; i <= number; i++)
            {
                int sum = 0;
                int digit = i;
                while (digit>0)
                {
                    sum += digit % 10;
                    digit /= 10;
                }
                if (sum==5 || sum==7 || sum==11)
                {
                    Console.WriteLine($"{i} -> True");
                }
                else
                {
                    Console.WriteLine($"{i} -> False");
                }
            }
        }
    }
}
